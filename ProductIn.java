    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        if(ProductInTable.getSelectedRow() >= 0){
        df = (DefaultTableModel)ProductInTable.getModel();
            int selected = ProductInTable.getSelectedRow();
            int id = Integer.parseInt(df.getValueAt(selected,0).toString());
            
            try {
                // TODO add your handling code here
                
                pst = con.prepareStatement("delete from product_in where id = ?");
                pst.setInt(1,id);
                
                
                int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this Product In record", "Remove Product In Record", JOptionPane.YES_NO_OPTION);
                if(confirm == JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(null, "Record Deleted successfully","Remove Product In Record",JOptionPane.INFORMATION_MESSAGE);
                pst.executeUpdate();
                txt_barcode.setText("");
                txt_ProductName.setText("");
                txt_cost.setText("");
                txt_quantity.setText("");
                txt_cost.setText("");
                txt_barcode.requestFocus();
                getProductsIn();
                saveBtn.setEnabled(true);
                
                lbl_barcode.setText("");
                lbl_quantity.setText("");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Select a record to delete and continue");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

