    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        if(supplierTable.getSelectedRow() >= 0){
            df = (DefaultTableModel)supplierTable.getModel();
            int selected = supplierTable.getSelectedRow();
            int id = Integer.parseInt(df.getValueAt(selected,0).toString());

             if(txt_firstname.getText().trim().isEmpty() && txt_lastname.getText().trim().isEmpty() && txt_phone.getText().trim().isEmpty() && txt_email.getText().trim().isEmpty() && txt_address.getText().trim().isEmpty()){
                lbl_firstname.setText("Firstname is required");
                lbl_lastname.setText("Lastname is required");
                lbl_phone.setText("Phone Number is required");
                lbl_email.setText("Email is required");
                lbl_address.setText("Address is required");
            }
             else if(txt_firstname.getText().trim().isEmpty() || txt_lastname.getText().trim().isEmpty() || txt_phone.getText().trim().isEmpty() || txt_email.getText().trim().isEmpty() || txt_address.getText().trim().isEmpty()){
                 JOptionPane.showMessageDialog(null, "Missing Information!!!.... Fill all fields before update");
             }
            else{
                try {
                    // TODO add your handling code here:  
                    pst = con.prepareStatement("update supplier set firstname = ?,lastname = ?,phone = ?,email = ? ,address = ? where id = ?");
                    pst.setString(1,txt_firstname.getText());
                    pst.setString(2,txt_lastname.getText());
                    pst.setString(3,txt_phone.getText());
                    pst.setString(4,txt_email.getText());
                    pst.setString(5,txt_address.getText());
                    pst.setInt(6,id);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Supplier information Updated successfully");

                    txt_firstname.setText("");
                    txt_lastname.setText("");
                    txt_phone.setText("");
                    txt_email.setText("");
                    txt_address.setText("");
                    txt_firstname.requestFocus();
                    getSuppliers();
                    saveBtn.setEnabled(true);
                    
                    lbl_firstname.setText("");
                    lbl_lastname.setText("");
                    lbl_phone.setText("");
                    lbl_email.setText("");
                    lbl_address.setText("");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
             }
        }
         else{
            JOptionPane.showMessageDialog(null, "Select a supplier to Update and procceed");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

