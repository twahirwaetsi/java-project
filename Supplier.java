    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
    if(supplierTable.getSelectedRow() >= 0){
        df = (DefaultTableModel)supplierTable.getModel();
        int selected = supplierTable.getSelectedRow();
        int id = Integer.parseInt(df.getValueAt(selected,0).toString());
        try {
            // TODO add your handling code here:  
            pst = con.prepareStatement("delete from supplier where id = ?");
            pst.setInt(1,id);
            
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this Supplier", "Remove Supplier", JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION){
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Supplier Deleted successfully","Remove Supplier",JOptionPane.INFORMATION_MESSAGE);
            txt_firstname.setText("");
            txt_lastname.setText("");
            txt_phone.setText("");
            txt_email.setText("");
            txt_address.setText("");
            txt_firstname.requestFocus();
            getSuppliers();
            saveBtn.setEnabled(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
        else{
            JOptionPane.showMessageDialog(null, "Select a supplier to delete and procceed");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

